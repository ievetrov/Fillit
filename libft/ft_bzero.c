#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*temp;
	int		i;

	i = 0;
	temp = (char *)s;
	if (n == 0)
		return ;
	while (n--)
	{
		temp[i] = 0;
		i++;
	}
}
